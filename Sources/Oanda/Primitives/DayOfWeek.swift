import Foundation


/// The DayOfWeek provides a representation of the day of the week.
public enum DayOfWeek: String, Codable {
				/// Sunday
				case sunday = "SUNDAY"
				/// Monday
				case monday = "MONDAY"
				/// Tuesday
				case tuesday = "TUESDAY"
				/// Wednesday
				case wednesday = "WEDNESDAY"
				/// Thursday
				case thursday = "THURSDAY"
				/// Friday
				case friday = "FRIDAY"
				/// Saturday
				case saturday = "SATURDAY"
}
